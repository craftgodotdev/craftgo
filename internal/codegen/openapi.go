// OpenAPI codegen uses `github.com/getkin/kin-openapi` to build a strongly-
// typed openapi3.T document and `sigs.k8s.io/yaml` to render it as YAML.
// The library handles spec compliance, default ordering, and the small
// quirks of OpenAPI 3.1; we only translate craftgo's AST into its types.
package codegen

import (
	"fmt"
	"net/http"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"

	"github.com/getkin/kin-openapi/openapi3"
	"sigs.k8s.io/yaml"

	"github.com/craftgodotdev/craftgo/internal/ast"
	"github.com/craftgodotdev/craftgo/internal/config"
	"github.com/craftgodotdev/craftgo/internal/semantic"
)

// GenerateOpenAPI builds an OpenAPI 3.1 document for pkg and writes it as
// YAML to the path configured by `output.openapi`. Each service contributes
// one set of operations under its `@prefix`; every concrete TypeDecl
// becomes a schema in `components.schemas`.
func GenerateOpenAPI(pkg *semantic.Package, cfg *config.Config, projectRoot string) error {
	if pkg.Name == "" {
		return fmt.Errorf("package has no name")
	}
	dest := filepath.Join(projectRoot, cfg.Output.OpenAPI)
	if err := os.MkdirAll(filepath.Dir(dest), 0o755); err != nil {
		return err
	}
	doc := buildOpenAPIDoc(pkg, cfg)
	out, err := yaml.Marshal(doc)
	if err != nil {
		return fmt.Errorf("marshal openapi: %w", err)
	}
	header := []byte("# Generated by craftgo. DO NOT EDIT.\n")
	return os.WriteFile(dest, append(header, out...), 0o644)
}

// GenerateProjectOpenAPI is the multi-package counterpart of
// [GenerateOpenAPI]: it merges every package's types/enums/errors/
// scalars/services into a single OpenAPI 3.1 document. When two
// packages declare a same-named entity, the second-and-subsequent
// occurrences get renamed to `<PascalPkg><Name>` (e.g. two packages
// each declaring `User` produce `User` for the first-seen and
// `AuthUser` / `SharedUser` for collisions). Non-conflicting names
// stay bare so simple projects keep readable schema names.
func GenerateProjectOpenAPI(proj *semantic.Project, cfg *config.Config, projectRoot string) error {
	if proj == nil {
		return nil
	}
	merged := mergeProjectForOpenAPI(proj)
	if merged.Name == "" {
		// Fallback for fully-empty projects - title must come from
		// somewhere so use the manifest or a sensible default.
		merged.Name = "design"
	}
	return GenerateOpenAPI(merged, cfg, projectRoot)
}

// mergeProjectForOpenAPI flattens every package in proj into a single
// synthetic [semantic.Package] suitable for feeding into the existing
// single-package OpenAPI generator. Name conflicts across packages
// are resolved by prefixing later occurrences with the
// PascalCase-d package name; type references inside merged decls are
// rewritten in lock-step so `$ref` values still point at the right
// schema after renaming.
//
// The merge is conservative: unique names stay bare, so a single-
// package project produces output identical to the legacy
// GenerateOpenAPI path. The conflict prefix only appears when the
// project actually has duplicates.
func mergeProjectForOpenAPI(proj *semantic.Project) *semantic.Package {
	out := &semantic.Package{
		Types:       map[string]*ast.TypeDecl{},
		Enums:       map[string]*ast.EnumDecl{},
		Errors:      map[string]*ast.ErrorDecl{},
		Scalars:     map[string]*ast.ScalarDecl{},
		Middlewares: map[string]*ast.MiddlewareDecl{},
		Services:    map[string]*semantic.ServiceInfo{},
	}
	pkgNames := make([]string, 0, len(proj.Packages))
	for n := range proj.Packages {
		if n != "" {
			pkgNames = append(pkgNames, n)
		}
	}
	sort.Strings(pkgNames)
	if len(pkgNames) > 0 {
		out.Name = pkgNames[0]
	}

	// Build the name-resolution table: for each (pkgName, origName)
	// tuple, the merged identifier the schema lives under. Conflicts
	// are detected by membership-counting across all packages: a
	// name is "shared" when it appears in 2+ packages.
	type symbolKey struct{ pkg, name string }
	resolve := map[symbolKey]string{}
	collide := func(name string) bool {
		count := 0
		for _, pn := range pkgNames {
			p := proj.Packages[pn]
			if p == nil {
				continue
			}
			if hasAnyDecl(p, name) {
				count++
				if count >= 2 {
					return true
				}
			}
		}
		return false
	}
	for _, pkgName := range pkgNames {
		p := proj.Packages[pkgName]
		if p == nil {
			continue
		}
		for _, name := range allDeclNames(p) {
			final := name
			if collide(name) {
				final = pascalCase(pkgName) + name
			}
			resolve[symbolKey{pkg: pkgName, name: name}] = final
		}
	}

	// Clone every decl into the merged package, rewriting the decl's
	// body type refs so `$ref` resolution still lines up. rewriteRef
	// takes the SOURCE package + a NamedTypeRef and returns either
	// the original ref (no rewrite needed) or a new ref with the
	// resolved single-part name.
	rewriteRef := func(srcPkg string, n *ast.NamedTypeRef) *ast.NamedTypeRef {
		if n == nil || n.Name == nil {
			return n
		}
		switch len(n.Name.Parts) {
		case 1:
			final, ok := resolve[symbolKey{pkg: srcPkg, name: n.Name.Parts[0]}]
			if !ok || final == n.Name.Parts[0] {
				return n
			}
			cp := *n
			cp.Name = &ast.QualifiedIdent{Pos: n.Name.Pos, Parts: []string{final}}
			return &cp
		case 2:
			final, ok := resolve[symbolKey{pkg: n.Name.Parts[0], name: n.Name.Parts[1]}]
			if !ok {
				return n
			}
			cp := *n
			cp.Name = &ast.QualifiedIdent{Pos: n.Name.Pos, Parts: []string{final}}
			return &cp
		}
		return n
	}

	for _, pkgName := range pkgNames {
		p := proj.Packages[pkgName]
		if p == nil {
			continue
		}
		for _, k := range sortedKeys(p.Types) {
			td := cloneTypeDecl(p.Types[k], resolve[symbolKey{pkg: pkgName, name: k}], pkgName, rewriteRef)
			out.Types[td.Name] = td
		}
		for _, k := range sortedKeys(p.Enums) {
			ed := *p.Enums[k]
			ed.Name = resolve[symbolKey{pkg: pkgName, name: k}]
			out.Enums[ed.Name] = &ed
		}
		for _, k := range sortedKeys(p.Errors) {
			ed := cloneErrorDecl(p.Errors[k], resolve[symbolKey{pkg: pkgName, name: k}], pkgName, rewriteRef)
			out.Errors[ed.Name] = ed
		}
		for _, k := range sortedKeys(p.Scalars) {
			sd := *p.Scalars[k]
			sd.Name = resolve[symbolKey{pkg: pkgName, name: k}]
			out.Scalars[sd.Name] = &sd
		}
		for name, si := range p.Services {
			final := name
			if _, dup := out.Services[final]; dup {
				final = pascalCase(pkgName) + name
			}
			out.Services[final] = cloneServiceInfo(si, pkgName, rewriteRef)
		}
		for name, md := range p.Middlewares {
			final := name
			if _, dup := out.Middlewares[final]; dup {
				final = pascalCase(pkgName) + name
			}
			out.Middlewares[final] = md
		}
	}
	return out
}

// hasAnyDecl reports whether p has a type/enum/error/scalar named
// `name`. Used by the collision detector during the OpenAPI merge.
func hasAnyDecl(p *semantic.Package, name string) bool {
	if _, ok := p.Types[name]; ok {
		return true
	}
	if _, ok := p.Enums[name]; ok {
		return true
	}
	if _, ok := p.Errors[name]; ok {
		return true
	}
	if _, ok := p.Scalars[name]; ok {
		return true
	}
	return false
}

// allDeclNames returns every type/enum/error/scalar name in p, in
// stable alphabetical order. Used to seed the resolve table during
// the OpenAPI merge.
func allDeclNames(p *semantic.Package) []string {
	seen := map[string]bool{}
	add := func(names ...string) {
		for _, n := range names {
			seen[n] = true
		}
	}
	for n := range p.Types {
		add(n)
	}
	for n := range p.Enums {
		add(n)
	}
	for n := range p.Errors {
		add(n)
	}
	for n := range p.Scalars {
		add(n)
	}
	out := make([]string, 0, len(seen))
	for n := range seen {
		out = append(out, n)
	}
	sort.Strings(out)
	return out
}

// sortedKeys returns the keys of m in alphabetical order. Used by
// the merge to produce deterministic schema ordering.
func sortedKeys[V any](m map[string]V) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}

// cloneTypeDecl deep-copies td and rewrites every named type ref in
// its body using rewrite (which carries srcPkg context). The
// returned decl is safe to mutate further.
func cloneTypeDecl(td *ast.TypeDecl, newName, srcPkg string, rewrite func(string, *ast.NamedTypeRef) *ast.NamedTypeRef) *ast.TypeDecl {
	cp := *td
	cp.Name = newName
	cp.Body = rewriteMembers(td.Body, srcPkg, rewrite)
	return &cp
}

// cloneErrorDecl mirrors [cloneTypeDecl] for errors.
func cloneErrorDecl(ed *ast.ErrorDecl, newName, srcPkg string, rewrite func(string, *ast.NamedTypeRef) *ast.NamedTypeRef) *ast.ErrorDecl {
	cp := *ed
	cp.Name = newName
	cp.Body = rewriteMembers(ed.Body, srcPkg, rewrite)
	return &cp
}

// rewriteMembers walks members, deep-copying every Field and Mixin
// with rewritten type refs. The original list is left untouched so
// upstream semantic results stay valid.
func rewriteMembers(members []ast.TypeMember, srcPkg string, rewrite func(string, *ast.NamedTypeRef) *ast.NamedTypeRef) []ast.TypeMember {
	out := make([]ast.TypeMember, 0, len(members))
	for _, m := range members {
		switch v := m.(type) {
		case *ast.Field:
			cp := *v
			cp.Type = rewriteTypeRef(v.Type, srcPkg, rewrite)
			out = append(out, &cp)
		case *ast.Mixin:
			cp := *v
			cp.Ref = rewrite(srcPkg, v.Ref)
			out = append(out, &cp)
		default:
			out = append(out, m)
		}
	}
	return out
}

// rewriteTypeRef descends into a TypeRef and applies rewrite to every
// embedded NamedTypeRef, including map keys/values and generic args.
// Returns a fresh tree so the caller can safely mutate further.
func rewriteTypeRef(t *ast.TypeRef, srcPkg string, rewrite func(string, *ast.NamedTypeRef) *ast.NamedTypeRef) *ast.TypeRef {
	if t == nil {
		return nil
	}
	cp := *t
	if t.Map != nil {
		mp := *t.Map
		mp.Key = rewriteTypeRef(t.Map.Key, srcPkg, rewrite)
		mp.Value = rewriteTypeRef(t.Map.Value, srcPkg, rewrite)
		cp.Map = &mp
	}
	if t.Named != nil {
		named := rewrite(srcPkg, t.Named)
		// Recurse into generic args so a nested cross-pkg ref is
		// also rewritten.
		if named != nil && len(named.Args) > 0 {
			args := make([]*ast.TypeRef, len(named.Args))
			for i, a := range named.Args {
				args[i] = rewriteTypeRef(a, srcPkg, rewrite)
			}
			ncp := *named
			ncp.Args = args
			named = &ncp
		}
		cp.Named = named
	}
	return &cp
}

// cloneServiceInfo shallow-clones a ServiceInfo and rewrites every
// method's request/response type so $ref resolution lines up after
// the merge's rename pass.
func cloneServiceInfo(si *semantic.ServiceInfo, srcPkg string, rewrite func(string, *ast.NamedTypeRef) *ast.NamedTypeRef) *semantic.ServiceInfo {
	out := *si
	out.Methods = make([]*ast.Method, len(si.Methods))
	for i, m := range si.Methods {
		cp := *m
		if m.Request != nil {
			cp.Request = rewrite(srcPkg, m.Request)
		}
		if m.Response != nil && m.Response.Type != nil {
			respCopy := *m.Response
			respCopy.Type = rewrite(srcPkg, m.Response.Type)
			cp.Response = &respCopy
		}
		out.Methods[i] = &cp
	}
	return &out
}

// pascalCase converts a DSL package name (commonly lowercase or
// kebab-cased) into its PascalCase form for OpenAPI schema prefixing.
// Empty / single-character inputs are passed through with the first
// rune uppercased.
func pascalCase(s string) string {
	if s == "" {
		return ""
	}
	var b []byte
	upNext := true
	for i := 0; i < len(s); i++ {
		c := s[i]
		if c == '-' || c == '_' || c == '/' {
			upNext = true
			continue
		}
		if upNext {
			if c >= 'a' && c <= 'z' {
				c -= 'a' - 'A'
			}
			upNext = false
		}
		b = append(b, c)
	}
	return string(b)
}

// buildOpenAPIDoc translates pkg + cfg into an openapi3.T document.
func buildOpenAPIDoc(pkg *semantic.Package, cfg *config.Config) *openapi3.T {
	doc := &openapi3.T{
		OpenAPI: "3.1.0",
		Info: &openapi3.Info{
			Title:   orDefault(cfg.OpenAPI.Title, pkg.Name),
			Version: orDefault(cfg.OpenAPI.Version, "0.1.0"),
		},
		Paths:      &openapi3.Paths{},
		Components: &openapi3.Components{Schemas: openapi3.Schemas{}},
	}
	if cfg.OpenAPI.BasePath != "" {
		doc.Servers = openapi3.Servers{{URL: cfg.OpenAPI.BasePath}}
	}
	addSchemas(doc, pkg)
	addPaths(doc, pkg)
	addSecuritySchemes(doc, pkg)
	return doc
}

// addSchemas populates components.schemas from every concrete TypeDecl,
// every EnumDecl, and every ScalarDecl. Generic type decls are skipped
// - there's no faithful OpenAPI 3.x representation for a parametric
// type, so generic INSTANCES are inlined at every reference site
// instead (see [schemaForTypeRef]).
//
// Enums and scalars MUST be emitted here too because a `$ref` from a
// field schema would otherwise dangle: `schemaForTypeRef` blindly
// renders `#/components/schemas/<Name>` for any named user type, and
// OpenAPI 3.x parsers (`kin-openapi`, swagger-cli, etc.) reject the
// resulting document with "key not found in object".
func addSchemas(doc *openapi3.T, pkg *semantic.Package) {
	addTypeSchemas(doc, pkg)
	addEnumSchemas(doc, pkg)
	addScalarSchemas(doc, pkg)
	addErrorSchemas(doc, pkg)
}

// addErrorSchemas emits one components.schemas entry per ErrorDecl so
// `@errors(Name)` references on methods can $ref a stable target. The
// shape mirrors the wire JSON the runtime emits: `code` (string),
// `message` (string), plus any user-declared custom field. The
// resulting schema name uses the smart-suffix rule (`UserNotFound` →
// `UserNotFoundErr`), matching the Go type name in errors.go.
func addErrorSchemas(doc *openapi3.T, pkg *semantic.Package) {
	names := make([]string, 0, len(pkg.Errors))
	for n := range pkg.Errors {
		names = append(names, n)
	}
	sort.Strings(names)
	for _, name := range names {
		ed := pkg.Errors[name]
		typeName := errSuffix(ed.Name)
		s := &openapi3.Schema{
			Type:       &openapi3.Types{"object"},
			Properties: openapi3.Schemas{},
			Description: fmt.Sprintf("%s error response (HTTP %d).",
				ed.Category, categoryStatus[ed.Category]),
		}
		// `code` / `message` are reserved DSL slots (design-time
		// override of the framework defaults via `@default(...)`) and
		// never appear on the wire - they're internal metadata exposed
		// through the `Code()` / `Error()` methods. Fields tagged with
		// `@header` / `@cookie` are also excluded - they ride on the
		// response writer (see [renderErrorResponseHeadersMethod]).
		// Anything else becomes a regular property on the schema.
		var mixinRefs openapi3.SchemaRefs
		for _, m := range ed.Body {
			switch v := m.(type) {
			case *ast.Field:
				if v.Name == "code" || v.Name == "message" {
					continue
				}
				if hasSensitiveDecorator(v.Decorators) {
					continue
				}
				switch bindingFromDecorators(v.Decorators) {
				case "header", "cookie":
					continue
				}
				s.Properties[v.Name] = schemaForTypeRef(v.Type, pkg)
			case *ast.Mixin:
				// Embedded mixin: same `allOf: [$ref]` shape that
				// `schemaForType` uses for TypeDecl, so error schemas
				// stay consistent with type schemas when a shared
				// mixin (`Timestamps`, audit fields, ...) is embedded.
				if v == nil || v.Ref == nil || v.Ref.Name == nil {
					continue
				}
				mixinRefs = append(mixinRefs, &openapi3.SchemaRef{
					Ref: "#/components/schemas/" + v.Ref.Name.String(),
				})
			}
		}
		if len(mixinRefs) > 0 {
			host := &openapi3.Schema{
				Type:       &openapi3.Types{"object"},
				Properties: s.Properties,
				Required:   s.Required,
			}
			mixinRefs = append(mixinRefs, &openapi3.SchemaRef{Value: host})
			s.Properties = nil
			s.Required = nil
			s.AllOf = mixinRefs
		}
		doc.Components.Schemas[typeName] = &openapi3.SchemaRef{Value: s}
	}
}

// addTypeSchemas emits one schema per concrete (non-generic) TypeDecl.
func addTypeSchemas(doc *openapi3.T, pkg *semantic.Package) {
	names := make([]string, 0, len(pkg.Types))
	for n := range pkg.Types {
		names = append(names, n)
	}
	sort.Strings(names)
	for _, name := range names {
		td := pkg.Types[name]
		if len(td.TypeParams) > 0 {
			continue
		}
		doc.Components.Schemas[name] = &openapi3.SchemaRef{Value: schemaForType(td, pkg)}
	}
}

// addEnumSchemas emits one schema per EnumDecl. The schema's base type
// is `string` for bare and string-valued enums, `integer` for int-valued.
// The OpenAPI `enum` array enumerates the wire values: bare values use
// the value name, string values use the literal, int values use the
// integer.
func addEnumSchemas(doc *openapi3.T, pkg *semantic.Package) {
	names := make([]string, 0, len(pkg.Enums))
	for n := range pkg.Enums {
		names = append(names, n)
	}
	sort.Strings(names)
	for _, name := range names {
		ed := pkg.Enums[name]
		s := &openapi3.Schema{Type: &openapi3.Types{"string"}}
		if firstEnumKind(ed) == ast.EnumInt {
			s.Type = &openapi3.Types{"integer"}
		}
		enumVals := ed.EnumValues()
		s.Enum = make([]any, 0, len(enumVals))
		for _, v := range enumVals {
			switch v.Kind {
			case ast.EnumInt:
				s.Enum = append(s.Enum, v.IntValue)
			case ast.EnumString:
				s.Enum = append(s.Enum, v.StrValue)
			default: // EnumBare - wire value is the source-side name
				s.Enum = append(s.Enum, v.Name)
			}
		}
		doc.Components.Schemas[name] = &openapi3.SchemaRef{Value: s}
	}
}

// addScalarSchemas emits one schema per ScalarDecl. The schema is
// the underlying primitive enriched with every decorator the scalar
// carries so OpenAPI consumers see the full contract:
//
//   - `@format(email)` → `format: email`
//   - `@length(1, 80)` / `@minLength(1)` / `@maxLength(80)` → minLength/maxLength
//   - `@pattern("...")` → pattern
//   - `@gte(0)` / `@lte(100)` / `@gt` / `@lt` / `@range(lo, hi)` → minimum/maximum (+exclusiveMin/Max)
//   - `@positive` / `@negative` → strict bound at 0
//   - `@multipleOf(N)` → multipleOf
//
// Without these the OpenAPI spec collapsed every scalar back to its
// bare primitive — the runtime validator enforced the rules but
// generated TS clients saw only `string` / `number` and could send
// values the server would reject.
func addScalarSchemas(doc *openapi3.T, pkg *semantic.Package) {
	names := make([]string, 0, len(pkg.Scalars))
	for n := range pkg.Scalars {
		names = append(names, n)
	}
	sort.Strings(names)
	for _, name := range names {
		sc := pkg.Scalars[name]
		base := primitiveSchema(sc.Primitive)
		if base == nil {
			base = &openapi3.Schema{Type: &openapi3.Types{"string"}}
		}
		applyPatternFormat(sc.Decorators, base)
		applyStringLengthConstraints(sc.Decorators, base)
		applyNumericConstraints(sc.Decorators, base)
		doc.Components.Schemas[name] = &openapi3.SchemaRef{Value: base}
	}
}

// schemaForType builds the openapi3.Schema for one TypeDecl. Only Field
// members are emitted; mixin expansion is a forward-looking concern.
//
// `@deprecated` propagates to OpenAPI in two places: type-level marks
// the entire schema as deprecated; field-level marks only that
// property. Tools like Swagger UI render deprecated entries with a
// strikethrough so consumers can spot them at a glance.
func schemaForType(td *ast.TypeDecl, pkg *semantic.Package) *openapi3.Schema {
	s := &openapi3.Schema{
		Type:       &openapi3.Types{"object"},
		Properties: openapi3.Schemas{},
	}
	s.Description = resolveDescription(td.Decorators, td.Doc)
	if hasDeprecatedDecorator(td.Decorators) {
		s.Deprecated = true
	}
	if ed := externalDocsFromDecorators(td.Decorators); ed != nil {
		s.ExternalDocs = ed
	}
	var mixinRefs openapi3.SchemaRefs
	for _, m := range td.Body {
		switch v := m.(type) {
		case *ast.Field:
			if hasSensitiveDecorator(v.Decorators) {
				continue
			}
			ref := schemaForTypeRef(v.Type, pkg)
			applyFieldMetadata(v, ref)
			s.Properties[v.Name] = ref
			if fieldIsRequired(v) {
				s.Required = append(s.Required, v.Name)
			}
		case *ast.Mixin:
			// Embedded mixin: OpenAPI 3.0 expresses Go's field-
			// promotion via `allOf: [$ref]` so the host schema
			// inherits every property of the referenced type.
			// Skipping the mixin would leave generated TS clients
			// without the embedded fields (`createdAt`/`updatedAt`,
			// ...) and runtime requests carrying them would fail
			// spec validation.
			if v == nil || v.Ref == nil || v.Ref.Name == nil {
				continue
			}
			mixinRefs = append(mixinRefs, &openapi3.SchemaRef{
				Ref: "#/components/schemas/" + v.Ref.Name.String(),
			})
		}
	}
	// Apply allOf with the mixin refs PLUS the host's own properties
	// when at least one mixin contributed. Without mixins we keep the
	// flat object shape so simple cases stay readable in YAML output.
	if len(mixinRefs) > 0 {
		host := &openapi3.Schema{
			Type:       &openapi3.Types{"object"},
			Properties: s.Properties,
			Required:   s.Required,
		}
		mixinRefs = append(mixinRefs, &openapi3.SchemaRef{Value: host})
		s.Properties = nil
		s.Required = nil
		s.AllOf = mixinRefs
	}
	return s
}

// applyFieldMetadata layers the OpenAPI-side decorator effects onto a
// property schema: doc → description, deprecated → flag + note,
// nullable → schema.Nullable, example → schema.Example,
// default → schema.Default.
//
// Both `T?` (optional) and `@nullable` translate to `nullable: true` in
// OpenAPI - the runtime decoder accepts JSON `null` for either shape,
// so the spec must let clients send it. The two forms still differ in
// `required[]` semantics (handled by [fieldIsRequired]): `?` drops the
// field from required, `@nullable` keeps it.
func applyFieldMetadata(f *ast.Field, ref *openapi3.SchemaRef) {
	if ref == nil {
		return
	}
	// Plain $ref: nothing to mutate on the field site - description,
	// example, default come from the referenced schema's own definition.
	if ref.Ref != "" {
		return
	}
	if ref.Value == nil {
		return
	}
	// Optional-ref wrapper (allOf:[$ref] + nullable: true). Cosmetic
	// metadata like description/example would land on the wrapper, which
	// tooling renders inconsistently - some UI generators show it next
	// to the field, others let the $ref's own description win. We drop
	// those decorators here; users who need a field-specific
	// description should alias the type (`type Manager = User`). But
	// `default` carries runtime semantics (server fills in when the
	// field is absent), so it stays on the wrapper regardless.
	if isAllOfRefWrapper(ref.Value) {
		if def, ok := defaultValue(f.Decorators); ok {
			ref.Value.Default = def
		}
		return
	}
	if desc := resolveDescription(f.Decorators, f.Doc); desc != "" {
		ref.Value.Description = desc
	}
	if hasDeprecatedDecorator(f.Decorators) {
		ref.Value.Deprecated = true
		if reason := deprecatedReason(f.Decorators); reason != "" {
			ref.Value.Description = appendDescription(ref.Value.Description, "Deprecated: "+reason)
		}
	}
	if hasNullableDecorator(f.Decorators) || (f.Type != nil && f.Type.Optional) {
		applyNullable(ref.Value)
	}
	if ex, ok := exampleValue(f.Decorators); ok {
		ref.Value.Example = ex
	}
	if def, ok := defaultValue(f.Decorators); ok {
		ref.Value.Default = def
	}
	applyNumericConstraints(f.Decorators, ref.Value)
	applyStringLengthConstraints(f.Decorators, ref.Value)
	applyArrayConstraints(f.Decorators, ref.Value)
	applyPatternFormat(f.Decorators, ref.Value)
}

// isAllOfRefWrapper recognises the `allOf: [{$ref}] + nullable: true`
// shape that schemaForTypeRef emits for an optional named-type field.
// The wrapper is the OpenAPI 3.0 idiom for "ref OR null"; metadata on
// the wrapper is interpreted inconsistently by clients, so callers
// branch on this signature to avoid stamping description/example on it.
func isAllOfRefWrapper(s *openapi3.Schema) bool {
	if s == nil || len(s.AllOf) != 1 || s.AllOf[0].Ref == "" {
		return false
	}
	return s.Type == nil && len(s.Properties) == 0
}

// applyNumericConstraints translates the numeric comparison decorators
// onto an OpenAPI schema. The mapping uses 3.0-style booleans for
// exclusive bounds because the kin-openapi validator we run against
// rejects 3.1's `{minimum: N, exclusiveMinimum: N}` shape — see the
// note on [applyNullable] for the same compatibility caveat.
//
//	@gte(N) → minimum: N
//	@gt(N)  → minimum: N, exclusiveMinimum: true
//	@lte(N) → maximum: N
//	@lt(N)  → maximum: N, exclusiveMaximum: true
//	@range(lo, hi) → minimum: lo, maximum: hi (both inclusive)
//	@multipleOf(N) → multipleOf: N
//	@positive → minimum: 0, exclusiveMinimum: true
//	@negative → maximum: 0, exclusiveMaximum: true
//
// Without this wiring, client generators (hey-api, openapi-typescript,
// openapi-generator) see the field as an unbounded `number` and produce
// types that allow values the server will reject at validate time.
func applyNumericConstraints(ds []*ast.Decorator, s *openapi3.Schema) {
	if s == nil {
		return
	}
	setMin := func(v float64, exclusive bool) {
		s.Min = &v
		if exclusive {
			s.ExclusiveMin = true
		}
	}
	setMax := func(v float64, exclusive bool) {
		s.Max = &v
		if exclusive {
			s.ExclusiveMax = true
		}
	}
	for _, d := range ds {
		if d == nil {
			continue
		}
		switch d.Name {
		case "gte":
			if v, ok := numericArgValue(d, 0); ok {
				setMin(v, false)
			}
		case "gt":
			if v, ok := numericArgValue(d, 0); ok {
				setMin(v, true)
			}
		case "lte":
			if v, ok := numericArgValue(d, 0); ok {
				setMax(v, false)
			}
		case "lt":
			if v, ok := numericArgValue(d, 0); ok {
				setMax(v, true)
			}
		case "range":
			if lo, ok := numericArgValue(d, 0); ok {
				setMin(lo, false)
			}
			if hi, ok := numericArgValue(d, 1); ok {
				setMax(hi, false)
			}
		case "positive":
			zero := 0.0
			s.Min = &zero
			s.ExclusiveMin = true
		case "negative":
			zero := 0.0
			s.Max = &zero
			s.ExclusiveMax = true
		case "multipleOf":
			if v, ok := numericArgValue(d, 0); ok && v != 0 {
				s.MultipleOf = &v
			}
		}
	}
}

// applyStringLengthConstraints maps `@length`/`@minLength`/`@maxLength`
// to the OpenAPI string keywords. Skipped on non-string schemas: caller
// has the field context, but emitting these on, say, a numeric schema
// would still validate (kin-openapi tolerates) — the guard is cheap.
func applyStringLengthConstraints(ds []*ast.Decorator, s *openapi3.Schema) {
	if s == nil {
		return
	}
	for _, d := range ds {
		if d == nil {
			continue
		}
		switch d.Name {
		case "minLength":
			if v, ok := numericArgValue(d, 0); ok && v >= 0 {
				u := uint64(v)
				s.MinLength = u
			}
		case "maxLength":
			if v, ok := numericArgValue(d, 0); ok && v >= 0 {
				u := uint64(v)
				s.MaxLength = &u
			}
		case "length":
			if lo, ok := numericArgValue(d, 0); ok && lo >= 0 {
				s.MinLength = uint64(lo)
			}
			if hi, ok := numericArgValue(d, 1); ok && hi >= 0 {
				u := uint64(hi)
				s.MaxLength = &u
			}
		}
	}
}

// applyArrayConstraints maps `@minItems` / `@maxItems` / `@uniqueItems`
// to the OpenAPI array keywords. No-op on non-array schemas — the
// caller's field context disambiguates but the schema itself doesn't
// reject these keywords on non-array shapes, so guarding here keeps
// the spec clean.
func applyArrayConstraints(ds []*ast.Decorator, s *openapi3.Schema) {
	if s == nil {
		return
	}
	for _, d := range ds {
		if d == nil {
			continue
		}
		switch d.Name {
		case "minItems":
			if v, ok := numericArgValue(d, 0); ok && v >= 0 {
				u := uint64(v)
				s.MinItems = u
			}
		case "maxItems":
			if v, ok := numericArgValue(d, 0); ok && v >= 0 {
				u := uint64(v)
				s.MaxItems = &u
			}
		case "uniqueItems":
			s.UniqueItems = true
		}
	}
}

// applyPatternFormat maps `@pattern("...")` and `@format(name)` to
// the OpenAPI keywords of the same name. Must be called for every
// field-level schema; scalar component schemas have their own emit
// path that sets these directly.
func applyPatternFormat(ds []*ast.Decorator, s *openapi3.Schema) {
	if s == nil {
		return
	}
	for _, d := range ds {
		if d == nil {
			continue
		}
		switch d.Name {
		case "pattern":
			if len(d.Args) == 1 {
				if sl, ok := d.Args[0].Value.(*ast.StringLit); ok {
					s.Pattern = sl.Value
				}
			}
		case "format":
			if len(d.Args) == 1 {
				switch v := d.Args[0].Value.(type) {
				case *ast.StringLit:
					s.Format = v.Value
				case *ast.IdentExpr:
					if v.Name != nil {
						s.Format = v.Name.String()
					}
				}
			}
		}
	}
}

// numericArgValue pulls the i-th positional argument as a float64.
// Accepts both IntLit and FloatLit so callers don't have to switch on
// type. Returns (0, false) for any other kind of literal so the OpenAPI
// emitter silently skips invalid args.
func numericArgValue(d *ast.Decorator, i int) (float64, bool) {
	if i >= len(d.Args) {
		return 0, false
	}
	switch v := d.Args[i].Value.(type) {
	case *ast.IntLit:
		return float64(v.Value), true
	case *ast.FloatLit:
		return v.Value, true
	}
	return 0, false
}

// defaultValue extracts a `@default(v)` argument as a typed Go value
// suitable for `openapi3.Schema.Default`. Mirrors [exampleValue] but
// keyed off the `@default` decorator. Returns (nil, false) when no
// default decorator is present so the caller leaves the schema
// untouched. Bare-ident defaults (e.g. `@default(Active)` for an enum)
// resolve to the ident's spelling - OpenAPI consumers see the wire
// value the runtime would emit.
func defaultValue(ds []*ast.Decorator) (any, bool) {
	for _, d := range ds {
		if d.Name != "default" || len(d.Args) == 0 {
			continue
		}
		v := d.Args[0].Value
		if ident, ok := v.(*ast.IdentExpr); ok && ident.Name != nil {
			return ident.Name.String(), true
		}
		return literalToAny(v)
	}
	return nil, false
}

// exampleValue extracts an `@example(v)` argument as a typed Go value
// suitable for `openapi3.Schema.Example`. Strings, ints, floats, and
// booleans all round-trip through YAML correctly when assigned to the
// `any` Example field. Array literals (`@example(["a", "b"])`) become
// `[]any` so array-typed properties get a sensible YAML rendering.
// Returns (nil, false) when no example decorator is present so the
// caller leaves the schema untouched.
func exampleValue(ds []*ast.Decorator) (any, bool) {
	for _, d := range ds {
		if d.Name != "example" || len(d.Args) == 0 {
			continue
		}
		return literalToAny(d.Args[0].Value)
	}
	return nil, false
}

// literalToAny converts an [ast.Expr] literal into the equivalent Go
// runtime value. Arrays recurse so nested literals (e.g. an array of
// ints) round-trip without losing element types. Unsupported nodes
// return (nil, false) and the caller skips emission.
func literalToAny(e ast.Expr) (any, bool) {
	switch v := e.(type) {
	case *ast.StringLit:
		return v.Value, true
	case *ast.IntLit:
		return v.Value, true
	case *ast.FloatLit:
		return v.Value, true
	case *ast.BoolLit:
		return v.Value, true
	case *ast.NullLit:
		return nil, true
	case *ast.ArrayLit:
		out := make([]any, 0, len(v.Elements))
		for _, el := range v.Elements {
			x, ok := literalToAny(el)
			if !ok {
				return nil, false
			}
			out = append(out, x)
		}
		return out, true
	}
	return nil, false
}

// hasNullableDecorator reports whether `@nullable` appears on the
// field. The DSL already has `T?` for "optional" (field can be absent);
// `@nullable` is the orthogonal "value can be null when present" flag,
// surfaced via OpenAPI's null-type entry so spec consumers know
// `null` is a valid wire value.
func hasNullableDecorator(ds []*ast.Decorator) bool {
	for _, d := range ds {
		if d.Name == "nullable" {
			return true
		}
	}
	return false
}

// hasSensitiveDecorator reports whether ds contains the `@sensitive`
// marker. Sensitive fields are server-only: they get `json:"-"` in
// the Go struct (so neither the JSON decoder nor the encoder touches
// them) and are skipped entirely from the OpenAPI spec.
func hasSensitiveDecorator(ds []*ast.Decorator) bool {
	for _, d := range ds {
		if d.Name == "sensitive" {
			return true
		}
	}
	return false
}

// applyNullable marks a schema as nullable. We emit the OpenAPI 3.0
// boolean form (`nullable: true`) even though our doc carries the
// `openapi: 3.1.0` header - kin-openapi 0.124's validator rejects the
// 3.1 canonical `type: [<base>, null]` array (it doesn't recognise
// "null" as a valid type entry). Once kin-openapi catches up, this
// helper can switch to appending "null" onto Schema.Type without any
// caller change.
func applyNullable(s *openapi3.Schema) {
	if s != nil {
		s.Nullable = true
	}
}

// externalDocsFromDecorators reads `@externalDocs(url: "…", description: "…")`
// off a chain. Both `url` and `description` are accepted as named args;
// missing url returns nil because OpenAPI treats `externalDocs.url` as
// required and consumers (Swagger UI) drop entries without it.
func externalDocsFromDecorators(ds []*ast.Decorator) *openapi3.ExternalDocs {
	for _, d := range ds {
		if d.Name != "externalDocs" {
			continue
		}
		var url, desc string
		for _, a := range d.Args {
			if !a.Named {
				continue
			}
			s, ok := a.Value.(*ast.StringLit)
			if !ok {
				continue
			}
			switch a.Name {
			case "url":
				url = s.Value
			case "description":
				desc = s.Value
			}
		}
		if url == "" {
			return nil
		}
		return &openapi3.ExternalDocs{URL: url, Description: desc}
	}
	return nil
}

// appendDescription joins a new note onto an existing description with
// a single blank-line separator. Empty existing description means the
// note becomes the entire description; empty note is a no-op.
func appendDescription(existing, note string) string {
	if note == "" {
		return existing
	}
	if existing == "" {
		return note
	}
	return existing + "\n\n" + note
}

// schemaForTypeRef converts an ast.TypeRef to a SchemaRef.
//
//   - Primitive DSL names (string, bool, int family, float family,
//     bytes, any, file) collapse to inline schemas.
//   - Generic instances (`Page<Book>`) are inlined by substituting the
//     type-param refs in the generic body - OpenAPI 3.x has no
//     parametric-schema concept, so $ref into the generic decl would
//     dangle.
//   - Plain named user types become `$ref` entries.
func schemaForTypeRef(t *ast.TypeRef, pkg *semantic.Package) *openapi3.SchemaRef {
	if t == nil {
		return &openapi3.SchemaRef{Value: &openapi3.Schema{Type: &openapi3.Types{"object"}}}
	}
	if t.Array {
		// Peel ONE bracket per recursion so multi-array types
		// (`Tag[][]`) emit nested OpenAPI `array` schemas. The
		// inner schemaForTypeRef call sees `Tag[]`, then `Tag`.
		// Clear Optional on the inner — `Tag[]?` means "the slice
		// may be absent", not "each element may be null"; leaving
		// the flag set would propagate `nullable: true` into the
		// items schema.
		inner := *t
		inner.Optional = false
		if inner.ArrayDepth > 0 {
			inner.ArrayDepth--
		}
		if inner.ArrayDepth == 0 {
			inner.Array = false
		}
		return &openapi3.SchemaRef{Value: &openapi3.Schema{
			Type:  &openapi3.Types{"array"},
			Items: schemaForTypeRef(&inner, pkg),
		}}
	}
	if t.Map != nil {
		return &openapi3.SchemaRef{Value: &openapi3.Schema{
			Type:                 &openapi3.Types{"object"},
			AdditionalProperties: openapi3.AdditionalProperties{Schema: schemaForTypeRef(t.Map.Value, pkg)},
		}}
	}
	if t.Named != nil {
		name := t.Named.Name.String()
		if prim := primitiveSchema(name); prim != nil {
			return &openapi3.SchemaRef{Value: prim}
		}
		if len(t.Named.Args) > 0 {
			if generic, ok := pkg.Types[name]; ok && len(generic.TypeParams) > 0 {
				return &openapi3.SchemaRef{Value: instantiateGeneric(generic, t.Named.Args, pkg)}
			}
		}
		// Bare `$ref` doesn't carry the nullable flag; the OpenAPI 3.0
		// idiom for "ref OR null" is `allOf: [$ref] + nullable: true`.
		// Without this wrapper, optional struct fields (`boss User?`)
		// emit a plain `$ref` and TS client generators type the field
		// as required `User`, refusing `null` even though the server
		// accepts it.
		if t.Optional {
			return &openapi3.SchemaRef{Value: &openapi3.Schema{
				AllOf:    openapi3.SchemaRefs{{Ref: "#/components/schemas/" + name}},
				Nullable: true,
			}}
		}
		return &openapi3.SchemaRef{Ref: "#/components/schemas/" + name}
	}
	return &openapi3.SchemaRef{Value: &openapi3.Schema{Type: &openapi3.Types{"object"}}}
}

// instantiateGeneric inlines a generic decl with the supplied type
// arguments. Each field's TypeRef is rebuilt with type-param names
// substituted by the matching concrete arg, then converted to a
// schema. The returned schema has no $ref of its own - the caller
// embeds it directly.
func instantiateGeneric(decl *ast.TypeDecl, args []*ast.TypeRef, pkg *semantic.Package) *openapi3.Schema {
	subst := map[string]*ast.TypeRef{}
	for i, p := range decl.TypeParams {
		if i < len(args) {
			subst[p] = args[i]
		}
	}
	s := &openapi3.Schema{
		Type:       &openapi3.Types{"object"},
		Properties: openapi3.Schemas{},
	}
	for _, m := range decl.Body {
		f, ok := m.(*ast.Field)
		if !ok {
			continue
		}
		if hasSensitiveDecorator(f.Decorators) {
			continue
		}
		s.Properties[f.Name] = schemaForTypeRef(substituteTypeRef(f.Type, subst), pkg)
		if fieldIsRequired(f) {
			s.Required = append(s.Required, f.Name)
		}
	}
	return s
}

// substituteTypeRef walks t and swaps every NamedTypeRef whose Name is
// a known type-param key with the matching concrete TypeRef. Array and
// Optional suffixes from the original survive; the substituted ref's
// own suffixes are merged in too (so `T?` substituted with `Book[]`
// correctly produces `Book[]?`).
func substituteTypeRef(t *ast.TypeRef, subst map[string]*ast.TypeRef) *ast.TypeRef {
	if t == nil {
		return nil
	}
	if t.Map != nil {
		return &ast.TypeRef{
			Pos: t.Pos,
			Map: &ast.MapType{
				Pos:   t.Map.Pos,
				Key:   substituteTypeRef(t.Map.Key, subst),
				Value: substituteTypeRef(t.Map.Value, subst),
			},
			Array:      t.Array,
			ArrayDepth: t.ArrayDepth,
			Optional:   t.Optional,
		}
	}
	if t.Named != nil {
		if rep, ok := subst[t.Named.Name.String()]; ok {
			out := *rep
			if t.Array {
				out.Array = true
				// Add the outer's array dim count on top of any
				// the substituted ref carried (e.g. `T?` →
				// `Book[]` becomes `Book[]?` with depth=1).
				if t.ArrayDepth > 0 {
					out.ArrayDepth += t.ArrayDepth
				} else if out.ArrayDepth == 0 {
					out.ArrayDepth = 1
				}
			}
			if t.Optional {
				out.Optional = true
			}
			return &out
		}
	}
	return t
}

// primitiveSchema returns an inline Schema for DSL primitive type names,
// or nil to signal "this is a user-defined type, emit a $ref".
func primitiveSchema(name string) *openapi3.Schema {
	switch name {
	case "string":
		return &openapi3.Schema{Type: &openapi3.Types{"string"}}
	case "bool":
		return &openapi3.Schema{Type: &openapi3.Types{"boolean"}}
	case "int", "int8", "int16", "int32", "int64", "uint", "uint8", "uint16", "uint32", "uint64":
		return &openapi3.Schema{Type: &openapi3.Types{"integer"}}
	case "float32", "float64":
		return &openapi3.Schema{Type: &openapi3.Types{"number"}}
	case "bytes":
		return &openapi3.Schema{Type: &openapi3.Types{"string"}, Format: "byte"}
	case "file":
		return &openapi3.Schema{Type: &openapi3.Types{"string"}, Format: "binary"}
	case "any":
		return &openapi3.Schema{}
	}
	return nil
}

// addPaths walks every service method and registers it as an operation
// under the matching path key, grouped by service via tags. Per-kind
// request schemas (`<Method>ReqBody`, `<Method>ReqQuery`, ...) are
// emitted into components.schemas as a side effect so operations can
// $ref them.
//
// Path keys here MUST be relative - basePath has already been pushed onto
// the servers entry, and OpenAPI 3.x resolves URLs as `servers[].url +
// path`. Passing basePath through `methodFullPath` would double-stamp the
// prefix in spec consumers (`/api` server + `/api/v1/foo` path →
// resolved `/api/api/v1/foo`).
func addPaths(doc *openapi3.T, pkg *semantic.Package) {
	for _, svcName := range sortedServices(pkg) {
		svc := pkg.Services[svcName]
		for _, m := range svc.Methods {
			full := methodFullPath("", svc.Primary, m)
			addPerOperationRequestSchemas(doc, m, pkg)
			addPerOperationResponseSchema(doc, m, pkg)
			item := doc.Paths.Value(full)
			if item == nil {
				item = &openapi3.PathItem{}
				doc.Paths.Set(full, item)
			}
			op := buildOperation(svcName, m, pkg)
			setOperation(item, m.Verb, op)
		}
	}
}

// fieldBins splits a request type's fields by binding kind. Empty slices
// are returned for kinds that have no contributors.
type fieldBins struct {
	body, query, header, cookie, path []*ast.Field
}

// binRequestFields walks the method's request type and partitions every
// field into the matching bin. The rules mirror runtime binding:
//   - Explicit @path / @query / @header / @cookie / @body / @form wins.
//   - A field whose name matches a `{param}` segment in the method path
//     is bound to `path`.
//   - Body verbs (POST/PUT/PATCH) keep unmarked fields in `body`.
//   - Non-body verbs (GET/DELETE/HEAD/OPTIONS) keep unmarked fields in
//     `query`.
func binRequestFields(m *ast.Method, pkg *semantic.Package) fieldBins {
	var bins fieldBins
	if m.Request == nil {
		return bins
	}
	td, ok := pkg.Types[m.Request.Name.String()]
	if !ok {
		return bins
	}
	pathNames := map[string]bool{}
	if m.Path != nil {
		for _, seg := range m.Path.Segments {
			if seg.Param {
				pathNames[seg.Literal] = true
			}
		}
	}
	bodyVerb := hasBodyVerb(m.Verb)
	for _, member := range td.Body {
		f, ok := member.(*ast.Field)
		if !ok {
			continue
		}
		if hasSensitiveDecorator(f.Decorators) {
			continue
		}
		switch bindingFromDecorators(f.Decorators) {
		case "path":
			bins.path = append(bins.path, f)
		case "query":
			bins.query = append(bins.query, f)
		case "header":
			bins.header = append(bins.header, f)
		case "cookie":
			bins.cookie = append(bins.cookie, f)
		case "body", "form":
			bins.body = append(bins.body, f)
		default:
			if pathNames[f.Name] {
				bins.path = append(bins.path, f)
			} else if bodyVerb {
				bins.body = append(bins.body, f)
			} else {
				bins.query = append(bins.query, f)
			}
		}
	}
	return bins
}

// addPerOperationRequestSchemas emits a grouped schema for every
// non-empty bin (`<Method>ReqBody`, `<Method>ReqQuery`,
// `<Method>ReqHeader`, `<Method>ReqCookie`, `<Method>ReqPath`). Each
// schema holds INLINE property definitions - making the per-kind schema
// the single canonical place where each field's type lives. Parameter
// `schema:` clauses then `$ref` into these per-kind schemas.
func addPerOperationRequestSchemas(doc *openapi3.T, m *ast.Method, pkg *semantic.Package) {
	bins := binRequestFields(m, pkg)
	if m.Request == nil {
		return
	}
	if len(bins.body) > 0 {
		doc.Components.Schemas[m.Name+"ReqBody"] = &openapi3.SchemaRef{Value: schemaFromFields(bins.body, pkg)}
	}
	if len(bins.query) > 0 {
		doc.Components.Schemas[m.Name+"ReqQuery"] = &openapi3.SchemaRef{Value: schemaFromFields(bins.query, pkg)}
	}
	if len(bins.header) > 0 {
		doc.Components.Schemas[m.Name+"ReqHeader"] = &openapi3.SchemaRef{Value: schemaFromFields(bins.header, pkg)}
	}
	if len(bins.cookie) > 0 {
		doc.Components.Schemas[m.Name+"ReqCookie"] = &openapi3.SchemaRef{Value: schemaFromFields(bins.cookie, pkg)}
	}
	if len(bins.path) > 0 {
		doc.Components.Schemas[m.Name+"ReqPath"] = &openapi3.SchemaRef{Value: schemaFromFields(bins.path, pkg)}
	}
}

// addPerOperationResponseSchema emits `<Method>RespBody` carrying the
// response shape consumers see in JSON. When the response type has no
// `@header` / `@cookie` bindings the schema is a thin alias of the type
// itself; when it does, header/cookie fields are stripped and only the
// JSON-body fields end up in the schema (the wire form the runtime
// serialises). The matching response.headers map is emitted by
// buildOperation.
func addPerOperationResponseSchema(doc *openapi3.T, m *ast.Method, pkg *semantic.Package) {
	if m.Response == nil || m.Response.Type == nil {
		return
	}
	bins := binResponseFields(m, pkg)
	if len(bins.header) == 0 && len(bins.cookie) == 0 {
		doc.Components.Schemas[m.Name+"RespBody"] = &openapi3.SchemaRef{
			Ref: "#/components/schemas/" + m.Response.Type.Name.String(),
		}
		return
	}
	doc.Components.Schemas[m.Name+"RespBody"] = &openapi3.SchemaRef{
		Value: schemaFromFields(bins.body, pkg),
	}
}

// binResponseFields partitions the response type's fields the same way
// [binRequestFields] does on the request side. Fields without an
// explicit response-side binding decorator default to `body` (the JSON
// payload), so adding @header / @cookie to a couple of fields does not
// silently drop the rest.
func binResponseFields(m *ast.Method, pkg *semantic.Package) fieldBins {
	var bins fieldBins
	if m.Response == nil || m.Response.Type == nil {
		return bins
	}
	td, ok := pkg.Types[m.Response.Type.Name.String()]
	if !ok {
		return bins
	}
	for _, member := range td.Body {
		f, ok := member.(*ast.Field)
		if !ok {
			continue
		}
		if hasSensitiveDecorator(f.Decorators) {
			continue
		}
		switch bindingFromDecorators(f.Decorators) {
		case "header":
			bins.header = append(bins.header, f)
		case "cookie":
			bins.cookie = append(bins.cookie, f)
		default:
			bins.body = append(bins.body, f)
		}
	}
	return bins
}

// buildResponseHeaders converts response-side @header / @cookie fields
// into the OpenAPI `response.headers` map. Cookie fields collapse into
// a single `Set-Cookie` entry because OpenAPI 3.x has no first-class
// cookie response slot — listing the cookie names there documents what
// the runtime writes via http.SetCookie even when the spec format has
// to round-trip through Set-Cookie.
func buildResponseHeaders(headers, cookies []*ast.Field, pkg *semantic.Package) openapi3.Headers {
	if len(headers) == 0 && len(cookies) == 0 {
		return nil
	}
	out := openapi3.Headers{}
	for _, f := range headers {
		name := headerNameFromDecorators(f.Decorators)
		if name == "" {
			name = f.Name
		}
		hdr := &openapi3.Header{
			Parameter: openapi3.Parameter{
				Schema:      schemaForTypeRef(f.Type, pkg),
				Description: resolveDescription(f.Decorators, f.Doc),
			},
		}
		out[name] = &openapi3.HeaderRef{Value: hdr}
	}
	if len(cookies) > 0 {
		names := make([]string, 0, len(cookies))
		for _, f := range cookies {
			n := cookieNameFromDecorators(f.Decorators)
			if n == "" {
				n = f.Name
			}
			names = append(names, n)
		}
		desc := "Sets cookies: " + strings.Join(names, ", ")
		out["Set-Cookie"] = &openapi3.HeaderRef{Value: &openapi3.Header{
			Parameter: openapi3.Parameter{
				Schema:      &openapi3.SchemaRef{Value: &openapi3.Schema{Type: &openapi3.Types{"string"}}},
				Description: desc,
			},
		}}
	}
	return out
}

// headerNameFromDecorators returns the literal name passed to
// `@header("X-Foo")` when present, otherwise empty so the caller
// falls back to the field name.
func headerNameFromDecorators(ds []*ast.Decorator) string {
	return decoratorStringArg(ds, "header")
}

// cookieNameFromDecorators mirrors [headerNameFromDecorators] for
// `@cookie("session_id")`.
func cookieNameFromDecorators(ds []*ast.Decorator) string {
	return decoratorStringArg(ds, "cookie")
}

// schemaFromFields builds an inline object schema covering the supplied
// fields. Required[] lists every non-optional field (required-by-default
// model — the inverse of the `?` suffix); nested types follow
// schemaForTypeRef. Per-field decorator effects (@default, @example,
// @nullable, @deprecated, @doc) are applied via [applyFieldMetadata] so
// per-operation `<Method>Req<Kind>` schemas carry the same metadata
// the top-level type schemas do.
func schemaFromFields(fields []*ast.Field, pkg *semantic.Package) *openapi3.Schema {
	s := &openapi3.Schema{
		Type:       &openapi3.Types{"object"},
		Properties: openapi3.Schemas{},
	}
	for _, f := range fields {
		if hasSensitiveDecorator(f.Decorators) {
			continue
		}
		ref := schemaForTypeRef(f.Type, pkg)
		applyFieldMetadata(f, ref)
		s.Properties[f.Name] = ref
		if fieldIsRequired(f) {
			s.Required = append(s.Required, f.Name)
		}
	}
	return s
}

// buildOperation assembles a single openapi3.Operation from a DSL method.
// Verb dispatch matches the runtime convention: body-bearing verbs
// (POST/PUT/PATCH) get a requestBody; the others expose request-type
// fields as path/query parameters so the spec mirrors the actual wire
// shape clients are expected to use.
func buildOperation(svcName string, m *ast.Method, pkg *semantic.Package) *openapi3.Operation {
	op := &openapi3.Operation{
		OperationID: operationID(m),
		Tags:        operationTags(svcName, m, pkg),
		Responses:   openapi3.NewResponses(),
		Description: resolveDescription(m.Decorators, m.Doc),
		Summary:     decoratorStringArg(m.Decorators, "summary"),
	}
	// Service-level `@externalDocs` is a single-value fallback: the
	// method-level decorator wins when present, otherwise the service's
	// own externalDocs applies to every operation it owns.
	svc := pkg.Services[svcName]
	if ed := externalDocsFromDecorators(m.Decorators); ed != nil {
		op.ExternalDocs = ed
	} else if svc != nil && svc.Primary != nil {
		if ed := externalDocsFromDecorators(svc.Primary.Decorators); ed != nil {
			op.ExternalDocs = ed
		}
	}
	// Service-level `@security` is appended to the method-level chain:
	// each entry in OpenAPI `security[]` is an OR alternative, so
	// declaring `@security(Bearer)` on the service plus
	// `@security(Admin)` on a method means "either Bearer alone OR Admin
	// alone unlocks this op". Use a sentinel `noauth` on the method to
	// override the inheritance entirely (current behaviour - emits an
	// empty requirement).
	var sec *openapi3.SecurityRequirements
	if svc != nil && svc.Primary != nil {
		sec = securityFromDecorators(svc.Primary.Decorators)
	}
	if methodSec := securityFromDecorators(m.Decorators); methodSec != nil {
		if sec == nil {
			sec = methodSec
		} else {
			combined := append(openapi3.SecurityRequirements{}, *sec...)
			combined = append(combined, *methodSec...)
			sec = &combined
		}
	}
	if sec != nil {
		op.Security = sec
	}
	// @deprecated may sit on the method itself or on the primary
	// service decl; either marks the operation deprecated. The
	// optional reason becomes a `Deprecated: ...` line in the
	// description so docs viewers surface it inline.
	deprecated := hasDeprecatedDecorator(m.Decorators)
	if !deprecated && svc != nil && svc.Primary != nil {
		deprecated = hasDeprecatedDecorator(svc.Primary.Decorators)
	}
	if deprecated {
		op.Deprecated = true
		reason := deprecatedReason(m.Decorators)
		if reason == "" && svc != nil && svc.Primary != nil {
			reason = deprecatedReason(svc.Primary.Decorators)
		}
		if reason != "" {
			op.Description = appendDescription(op.Description, "Deprecated: "+reason)
		}
	}
	isPassthrough := hasPassthroughDecorator(m.Decorators)
	isMultipart := false
	formStrings, formFiles := []paramBinding(nil), []paramBinding(nil)
	if m.Request != nil && !isPassthrough {
		if fs, ff := collectFormBindings(m, pkg); len(ff) > 0 {
			isMultipart = true
			formStrings, formFiles = fs, ff
		}
	}
	if m.Request != nil && !isPassthrough {
		bins := binRequestFields(m, pkg)
		// Body-bearing verbs $ref the per-method body schema. The
		// per-kind schemas live in components.schemas so consumers have
		// a single canonical reference for each binding kind.
		if hasBodyVerb(m.Verb) {
			switch {
			case isMultipart:
				op.RequestBody = multipartRequestBody(formStrings, formFiles)
			case len(bins.body) > 0:
				op.RequestBody = &openapi3.RequestBodyRef{Value: &openapi3.RequestBody{
					Required: true,
					Content: openapi3.Content{
						"application/json": &openapi3.MediaType{
							Schema: &openapi3.SchemaRef{Ref: "#/components/schemas/" + m.Name + "ReqBody"},
						},
					},
				}}
			}
		}
		// Parameters keep individual entries - that's the OpenAPI norm -
		// but each field's `schema:` $refs into the matching
		// `<Method>Req<Kind>` schema, which holds the canonical
		// definition. Multipart skips path/query/header params here too
		// since the form-data body covers the regular fields; only true
		// path/query/header bindings remain (handled in paramsFromBins).
		if !isMultipart {
			op.Parameters = paramsFromBins(bins, pkg)
		} else {
			op.Parameters = paramsFromBins(fieldBins{path: bins.path, query: bins.query, header: bins.header, cookie: bins.cookie}, pkg)
		}
	}
	if isPassthrough {
		op.Parameters = passthroughPathParams(m)
	}
	successCode := successStatus(m)
	switch {
	case isPassthrough:
		desc := successDescription(successCode)
		op.Responses.Set(successCode, &openapi3.ResponseRef{Value: &openapi3.Response{
			Description: &desc,
			Content: openapi3.Content{
				"*/*": &openapi3.MediaType{},
			},
		}})
	case m.Response != nil && m.Response.Type != nil:
		desc := successDescription(successCode)
		resp := &openapi3.Response{
			Description: &desc,
			Content: openapi3.Content{
				"application/json": &openapi3.MediaType{
					// Per the request-side convention, the response body
					// is referenced via `<Method>RespBody` so consumers
					// have a stable, per-operation $ref target.
					Schema: &openapi3.SchemaRef{Ref: "#/components/schemas/" + m.Name + "RespBody"},
				},
			},
		}
		if respBins := binResponseFields(m, pkg); len(respBins.header) > 0 || len(respBins.cookie) > 0 {
			resp.Headers = buildResponseHeaders(respBins.header, respBins.cookie, pkg)
		}
		op.Responses.Set(successCode, &openapi3.ResponseRef{Value: resp})
	default:
		fallback := "204"
		if successCode != "200" {
			fallback = successCode
		}
		desc := successDescription(fallback)
		op.Responses.Set(fallback, &openapi3.ResponseRef{Value: &openapi3.Response{Description: &desc}})
	}
	addErrorResponses(op, m, pkg)
	return op
}

// successDescription returns the IANA-registered reason phrase for an
// HTTP status code so OpenAPI clients see `Created` for 201, `No Content`
// for 204, etc. Falls back to "OK" for unknown codes - a generic but
// valid placeholder is better than an empty description (which some
// validators flag as required).
func successDescription(code string) string {
	n, err := strconv.Atoi(code)
	if err != nil {
		return "OK"
	}
	if text := http.StatusText(n); text != "" {
		return text
	}
	return "OK"
}

// successStatus returns the HTTP status code key for the success
// response. `@status(N)` overrides the default; otherwise body verbs
// without a response default to 204 elsewhere, and everything else
// to 200 here.
func successStatus(m *ast.Method) string {
	for _, d := range m.Decorators {
		if d.Name != "status" || len(d.Args) == 0 {
			continue
		}
		if i, ok := d.Args[0].Value.(*ast.IntLit); ok {
			return strconv.FormatInt(i.Value, 10)
		}
	}
	return "200"
}

// addErrorResponses walks `@errors(NameA, NameB)` on the method and
// adds one OpenAPI response entry per declared error type. The status
// code comes from the error's category (categoryStatus) and the schema
// $refs the error type's components.schemas entry. Unknown error names
// are silently skipped - semantic phase doesn't validate the refs yet,
// so we treat that as best-effort docs rather than fail codegen.
func addErrorResponses(op *openapi3.Operation, m *ast.Method, pkg *semantic.Package) {
	names := errorRefsFromDecorators(m.Decorators)
	if len(names) == 0 {
		return
	}
	for _, name := range names {
		ed, ok := pkg.Errors[name]
		if !ok {
			continue
		}
		typeName := errSuffix(ed.Name)
		status := strconv.Itoa(categoryStatus[ed.Category])
		desc := ed.Category
		op.Responses.Set(status, &openapi3.ResponseRef{Value: &openapi3.Response{
			Description: &desc,
			Content: openapi3.Content{
				"application/json": &openapi3.MediaType{
					Schema: &openapi3.SchemaRef{Ref: "#/components/schemas/" + typeName},
				},
			},
		}})
	}
}

// errorRefsFromDecorators flattens every `@errors(NameA, NameB, ...)`
// chain on the method into a deduplicated list of error declaration
// names. Both the bare-ident form (`@errors(Foo)`) and the
// fully-qualified `pkg.Foo` form parse here — qualified refs
// collapse to the trailing segment because cross-package resolution
// isn't yet supported.
func errorRefsFromDecorators(ds []*ast.Decorator) []string {
	seen := map[string]bool{}
	var out []string
	for _, d := range ds {
		if d.Name != "errors" {
			continue
		}
		for _, a := range d.Args {
			id, ok := a.Value.(*ast.IdentExpr)
			if !ok || id.Name == nil {
				continue
			}
			name := id.Name.Parts[len(id.Name.Parts)-1]
			if seen[name] {
				continue
			}
			seen[name] = true
			out = append(out, name)
		}
	}
	return out
}

// passthroughPathParams emits one OpenAPI path-parameter entry per
// `{name}` segment in the route. Passthrough endpoints have no
// request type to mine for typed parameters, so the schema is the
// minimal `string` placeholder - enough to render Swagger UI's
// "try it" form without lying about the wire shape.
func passthroughPathParams(m *ast.Method) openapi3.Parameters {
	if m.Path == nil {
		return nil
	}
	var params openapi3.Parameters
	for _, seg := range m.Path.Segments {
		if !seg.Param {
			continue
		}
		params = append(params, &openapi3.ParameterRef{Value: &openapi3.Parameter{
			Name:     seg.Literal,
			In:       "path",
			Required: true,
			Schema: &openapi3.SchemaRef{Value: &openapi3.Schema{
				Type: &openapi3.Types{"string"},
			}},
		}})
	}
	return params
}

// multipartRequestBody renders a `multipart/form-data` schema covering
// every plain-text form field plus every `file` field declared on the
// request type. File fields use `format: binary`, which Swagger UI
// renders as a file picker.
//
// Files carrying `@mimeTypes(["a/b", "c/d"])` surface their allowlist
// under the OpenAPI `encoding[field].contentType` slot — without this
// the client SDK has no way to see what MIME types the server's
// runtime validator will accept, so users would upload an arbitrary
// file and get a 400 from the validator instead of a typed rejection
// at SDK call time.
func multipartRequestBody(forms, files []paramBinding) *openapi3.RequestBodyRef {
	props := openapi3.Schemas{}
	for _, f := range forms {
		props[f.DSLName] = &openapi3.SchemaRef{Value: &openapi3.Schema{
			Type: &openapi3.Types{"string"},
		}}
	}
	encoding := map[string]*openapi3.Encoding{}
	for _, f := range files {
		props[f.DSLName] = &openapi3.SchemaRef{Value: &openapi3.Schema{
			Type:   &openapi3.Types{"string"},
			Format: "binary",
		}}
		if len(f.MimeTypes) > 0 {
			encoding[f.DSLName] = &openapi3.Encoding{
				ContentType: strings.Join(f.MimeTypes, ", "),
			}
		}
	}
	mt := &openapi3.MediaType{
		Schema: &openapi3.SchemaRef{Value: &openapi3.Schema{
			Type:       &openapi3.Types{"object"},
			Properties: props,
		}},
	}
	if len(encoding) > 0 {
		mt.Encoding = encoding
	}
	return &openapi3.RequestBodyRef{Value: &openapi3.RequestBody{
		Required: true,
		Content:  openapi3.Content{"multipart/form-data": mt},
	}}
}

// paramsFromBins flattens the non-body bins into the `parameters[]`
// slice the OpenAPI spec requires. Path is always required; query /
// header / cookie required flags follow the field's optionality (the
// inverse of `?`). Each parameter's schema is emitted inline (by value)
// rather than `$ref`-ing into the wrapper `<Method>Req<Kind>` schema.
// Nested `$ref` (into `.../properties/<name>`) is technically valid
// JSON-Pointer but many TS / Java / Rust client generators (hey-api,
// openapi-typescript, openapi-generator < 7) fail to derive a stable
// type name from the property-walk path and emit anonymous placeholders
// or drop the type entirely. Inlining keeps the spec portable; the
// wrapper schemas stay in `components.schemas` for tooling that wants
// to ref the full request shape.
func paramsFromBins(bins fieldBins, pkg *semantic.Package) openapi3.Parameters {
	var params openapi3.Parameters
	add := func(in string, fields []*ast.Field, alwaysRequired bool) {
		for _, f := range fields {
			required := alwaysRequired || fieldIsRequired(f)
			ref := schemaForTypeRef(f.Type, pkg)
			applyFieldMetadata(f, ref)
			params = append(params, &openapi3.ParameterRef{Value: &openapi3.Parameter{
				Name:     f.Name,
				In:       in,
				Required: required,
				Schema:   ref,
			}})
		}
	}
	add("path", bins.path, true)
	add("query", bins.query, false)
	add("header", bins.header, false)
	add("cookie", bins.cookie, false)
	return params
}

// bindingFromDecorators returns the OpenAPI `in` string implied by a
// field-binding decorator, or "" when the field has no explicit binding.
// `@body` and `@form` are returned verbatim so the caller can recognise
// and skip them - body-shaped fields land in requestBody, not parameters.
func bindingFromDecorators(ds []*ast.Decorator) string {
	for _, d := range ds {
		switch d.Name {
		case "path":
			return "path"
		case "query":
			return "query"
		case "header":
			return "header"
		case "cookie":
			return "cookie"
		case "body":
			return "body"
		case "form":
			return "form"
		}
	}
	return ""
}

// setOperation routes a built operation onto the right verb slot.
func setOperation(item *openapi3.PathItem, verb string, op *openapi3.Operation) {
	switch strings.ToUpper(verb) {
	case "GET":
		item.Get = op
	case "POST":
		item.Post = op
	case "PUT":
		item.Put = op
	case "PATCH":
		item.Patch = op
	case "DELETE":
		item.Delete = op
	case "HEAD":
		item.Head = op
	case "OPTIONS":
		item.Options = op
	}
}

// fieldIsRequired reports whether f must be present in the request
// payload. craftgo's "required by default" model: a field is required
// unless its type carries the `?` suffix that explicitly marks it
// optional.
func fieldIsRequired(f *ast.Field) bool {
	return f != nil && f.Type != nil && !f.Type.Optional
}

// operationID returns the OpenAPI operationId for the method. Default
// is the method's source-side name (e.g. `CreateProfile`); a method
// decorated with `@operationId("createUserProfile")` overrides the
// default with the supplied string literal. The override is honoured
// verbatim so projects can adopt camelCase / kebab-case / whatever
// convention their tooling expects.
func operationID(m *ast.Method) string {
	for _, d := range m.Decorators {
		if d.Name != "operationId" || len(d.Args) == 0 {
			continue
		}
		if s, ok := d.Args[0].Value.(*ast.StringLit); ok && s.Value != "" {
			return s.Value
		}
	}
	return m.Name
}

// operationTags assembles the OpenAPI `tags:` slice for one method.
// Service-level `@tags(...)` come first (so they sort before method
// tags in the resulting spec), then method-level `@tags(...)` are
// appended. When neither level declares tags the service name is used
// as a single default - keeping every operation grouped by service for
// tools that don't render an empty tag list well.
func operationTags(svcName string, m *ast.Method, pkg *semantic.Package) []string {
	seen := map[string]bool{}
	var out []string
	add := func(t string) {
		if t == "" || seen[t] {
			return
		}
		seen[t] = true
		out = append(out, t)
	}
	if svc, ok := pkg.Services[svcName]; ok && svc.Primary != nil {
		for _, t := range tagsFromDecorators(svc.Primary.Decorators) {
			add(t)
		}
	}
	for _, t := range tagsFromDecorators(m.Decorators) {
		add(t)
	}
	if len(out) == 0 {
		out = []string{svcName}
	}
	return out
}

// tagsFromDecorators collects every argument from every `@tags(...)`
// decorator in ds. Arguments may be string literals (`@tags("v1")`) or
// bare identifiers (`@tags(api, v1)`); both shapes produce the same
// stringified entry in the resulting slice.
func tagsFromDecorators(ds []*ast.Decorator) []string {
	var out []string
	for _, d := range ds {
		if d.Name != "tags" {
			continue
		}
		for _, a := range d.Args {
			switch v := a.Value.(type) {
			case *ast.StringLit:
				out = append(out, v.Value)
			case *ast.IdentExpr:
				out = append(out, v.Name.String())
			}
		}
	}
	return out
}

// securityFromDecorators turns `@security(SchemeA, SchemeB)` declarations
// on a method or service into the OpenAPI `security` slice. Each
// decorator argument that is an identifier becomes one entry whose value
// is an empty scopes list - multi-scheme arguments inside a single
// decorator are AND-combined; multiple `@security(...)` decorators are
// OR-combined per the OpenAPI spec semantics. The special name
// `noauth` produces an empty Requirement, telling the OpenAPI consumer
// the endpoint is explicitly public.
func securityFromDecorators(ds []*ast.Decorator) *openapi3.SecurityRequirements {
	var reqs openapi3.SecurityRequirements
	for _, d := range ds {
		if d.Name != "security" {
			continue
		}
		req := openapi3.SecurityRequirement{}
		for _, a := range d.Args {
			if id, ok := a.Value.(*ast.IdentExpr); ok {
				name := id.Name.String()
				if name == "noauth" {
					continue
				}
				req[name] = []string{}
			}
		}
		reqs = append(reqs, req)
	}
	if len(reqs) == 0 {
		return nil
	}
	return &reqs
}

// addSecuritySchemes walks every service + method, collects the unique
// scheme names referenced via `@security(...)`, and emits a default
// `components.securitySchemes` entry per name. The default scheme is a
// `http: bearer` definition because that's what the e2e middleware
// (and most production deployments) actually use; projects can override
// the shape later via a manifest-level `securitySchemes:` block.
func addSecuritySchemes(doc *openapi3.T, pkg *semantic.Package) {
	if doc.Components == nil {
		doc.Components = &openapi3.Components{}
	}
	if doc.Components.SecuritySchemes == nil {
		doc.Components.SecuritySchemes = openapi3.SecuritySchemes{}
	}
	collect := func(ds []*ast.Decorator, into map[string]bool) {
		for _, d := range ds {
			if d.Name != "security" {
				continue
			}
			for _, a := range d.Args {
				if id, ok := a.Value.(*ast.IdentExpr); ok {
					n := id.Name.String()
					if n != "noauth" {
						into[n] = true
					}
				}
			}
		}
	}
	names := map[string]bool{}
	for _, svc := range pkg.Services {
		if svc.Primary != nil {
			collect(svc.Primary.Decorators, names)
		}
		for _, m := range svc.Methods {
			collect(m.Decorators, names)
		}
	}
	for n := range names {
		if _, exists := doc.Components.SecuritySchemes[n]; exists {
			continue
		}
		doc.Components.SecuritySchemes[n] = &openapi3.SecuritySchemeRef{Value: &openapi3.SecurityScheme{
			Type:         "http",
			Scheme:       "bearer",
			BearerFormat: "JWT",
		}}
	}
}

// ValidateSecurityRefs cross-checks every `@security(scheme)` reference
// in pkg against the manifest's declared `openapi.securitySchemes` map.
// The check is permissive when the manifest declares no schemes: in
// that case we keep the legacy auto-generated bearer behaviour (so
// projects that haven't migrated continue to work). When the manifest
// HAS declared at least one scheme, every reference must resolve to a
// key in that map; unknown references produce a sorted list of error
// strings the caller can format.
//
// The special name `noauth` is always accepted - it is a marker, not a
// scheme reference.
func ValidateSecurityRefs(pkg *semantic.Package, cfg *config.Config) []string {
	if cfg == nil || len(cfg.OpenAPI.SecuritySchemes) == 0 {
		return nil
	}
	declared := cfg.OpenAPI.SecuritySchemes
	collect := func(svcName, scope string, ds []*ast.Decorator, dst map[string]bool) {
		for _, d := range ds {
			if d.Name != "security" {
				continue
			}
			for _, a := range d.Args {
				id, ok := a.Value.(*ast.IdentExpr)
				if !ok {
					continue
				}
				name := id.Name.String()
				if name == "noauth" {
					continue
				}
				if _, exists := declared[name]; exists {
					continue
				}
				key := svcName + "/" + scope + "/" + name
				dst[key] = true
			}
		}
	}
	bad := map[string]bool{}
	for svcName, svc := range pkg.Services {
		if svc.Primary != nil {
			collect(svcName, "service", svc.Primary.Decorators, bad)
		}
		for _, m := range svc.Methods {
			collect(svcName, "method "+m.Name, m.Decorators, bad)
		}
	}
	if len(bad) == 0 {
		return nil
	}
	out := make([]string, 0, len(bad))
	for k := range bad {
		parts := strings.SplitN(k, "/", 3)
		// parts: svc, scope, name
		out = append(out, fmt.Sprintf("@security(%s) on %s %s: scheme %q is not declared in openapi.securitySchemes", parts[2], parts[1], parts[0], parts[2]))
	}
	sort.Strings(out)
	return out
}

// orDefault returns v when non-empty, otherwise fallback.
func orDefault(v, fallback string) string {
	if v == "" {
		return fallback
	}
	return v
}
