// GenerateErrors emits a single `errors.go` file under outDir/<pkg.Name>/
// declaring one struct + constructor + Error()/HTTPStatus() methods +
// SCREAMING_SNAKE error-code constant for every [ast.ErrorDecl] in pkg.
// When pkg has no errors the function is a no-op.
func GenerateErrors(pkg *semantic.Package, outDir string) error {
	if pkg.Name == "" {
		return fmt.Errorf("package has no name")
	}
	if len(pkg.Errors) == 0 {
		return nil
	}
	pkgDir := filepath.Join(outDir, pkg.Name)
	if err := os.MkdirAll(pkgDir, 0o755); err != nil {
		return err
	}
	src := buildErrorsGo(pkg)
	formatted, err := format.Source([]byte(src))
	if err != nil {
		return fmt.Errorf("format errors.go: %w\n--- source ---\n%s", err, src)
	}
	return os.WriteFile(filepath.Join(pkgDir, "errors.go"), formatted, 0o644)
}

// buildErrorsGo assembles errors.go in alphabetical name order. No
// imports are emitted because the basic error shape needs none. The
// result is returned pre-formatting; the caller runs `go/format` to
// normalise whitespace.
func buildErrorsGo(pkg *semantic.Package) string {
	parts := []string{
		"// Code generated by craftgo. DO NOT EDIT.\n",
		"package " + pkg.Name + "\n",
	}
	names := make([]string, 0, len(pkg.Errors))
	for n := range pkg.Errors {
		names = append(names, n)
	}
	sort.Strings(names)
	for _, name := range names {
		parts = append(parts, renderError(pkg.Errors[name]))
	}
	return strings.Join(parts, "\n")
}

// renderError returns the full Go scaffolding for one error declaration:
//
//	const ErrCode<Name> = "<SCREAMING_SNAKE>"
//	type <Name>Err struct { Code, Message string; ...customFields }
//	func New<Name>Err(...customFields) *<Name>Err { ... }
//	func (e *<Name>Err) Error() string       { return e.Message }
//	func (e *<Name>Err) HTTPStatus() int     { return <statusCode> }
//	func (e *<Name>Err) WithMessage(...) *<Name>Err
//	func (e *<Name>Err) WithCode(...)    *<Name>Err
//
// User-declared `code` / `message` fields with `@default(...)` override
// the derived defaults; any other declared field becomes a constructor
// parameter and a struct field with a JSON tag matching the DSL name.
func renderError(ed *ast.ErrorDecl) string {
	typeName := errSuffix(ed.Name)
	constName := "ErrCode" + ed.Name
	code := screamingSnake(ed.Name)
	msg := categoryMessage[ed.Category]
	status := categoryStatus[ed.Category]
	customFields := errorCustomFields(ed, &code, &msg)

	return fmt.Sprintf(`// %[1]s is the canonical machine-readable code for %[2]s.
const %[1]s = %[3]s

// %[2]s is the typed %[4]s error generated for `+"`%[5]s`"+`.
type %[2]s struct {
	Code    string `+"`json:\"code\"`"+`
	Message string `+"`json:\"message\"`"+`
%[6]s}

// New%[2]s constructs %[2]s with the canonical code and message.
func New%[2]s(%[7]s) *%[2]s {
	return &%[2]s{
		Code:    %[1]s,
		Message: %[8]s,
%[9]s	}
}

// Error implements the standard error interface.
func (e *%[2]s) Error() string { return e.Message }

// HTTPStatus returns the HTTP status code associated with the %[4]s category.
func (e *%[2]s) HTTPStatus() int { return %[10]d }

// WithMessage replaces the default Message and returns the receiver
// so callers can chain it onto a return statement.
func (e *%[2]s) WithMessage(msg string) *%[2]s {
	e.Message = msg
	return e
}

// WithCode replaces the default Code and returns the receiver so
// callers can chain it onto a return statement. Prefer the design-time
// `+"`code @default(...)`"+` form when the override is constant.
func (e *%[2]s) WithCode(code string) *%[2]s {
	e.Code = code
	return e
}
`,
		constName,                          // [1] ErrCode<Name>
		typeName,                           // [2] <Name>Err
		strconv.Quote(code),                // [3] quoted code value
		ed.Category,                        // [4] human-readable category
		ed.Name,                            // [5] DSL name (for the struct doc)
		renderErrorCustomStructFields(customFields), // [6] extra struct fields
		renderErrorCtorParams(customFields),         // [7] constructor parameter list
		strconv.Quote(msg),                          // [8] quoted default message
		renderErrorCtorAssigns(customFields),        // [9] extra ctor assignments
		status,                                      // [10] HTTP status
	)
}

// errorCustomFields collects every non-`code` / non-`message` field
// from the error body. `@default(...)` on `code` / `message` overrides
// the supplied pointers in-place so the caller observes the user's
// chosen value.
func errorCustomFields(ed *ast.ErrorDecl, code, msg *string) []*ast.Field {
	var out []*ast.Field
	for _, m := range ed.Body {
		f, ok := m.(*ast.Field)
		if !ok {
			continue
		}
		switch f.Name {
		case "code":
			if v := stringDefault(f.Decorators); v != "" {
				*code = v
			}
		case "message":
			if v := stringDefault(f.Decorators); v != "" {
				*msg = v
			}
		default:
			out = append(out, f)
		}
	}
	return out
}

// renderErrorCustomStructFields returns the indented struct-body lines
// for every custom field — one `Name Type \`json:"name"\`` per line.
// Empty when no custom fields exist; the surrounding template still
// produces a valid struct because `Code` / `Message` are always present.
func renderErrorCustomStructFields(fields []*ast.Field) string {
	if len(fields) == 0 {
		return ""
	}
	var sb strings.Builder
	for _, f := range fields {
		sb.WriteString(fmt.Sprintf("\t%s %s `json:%s`\n",
			GoFieldName(f.Name), GoTypeRef(f.Type), strconv.Quote(f.Name)))
	}
	return sb.String()
}

// renderErrorCtorParams returns the comma-separated constructor
// parameter list ("name string, age int") or "" when the error has no
// custom fields.
func renderErrorCtorParams(fields []*ast.Field) string {
	if len(fields) == 0 {
		return ""
	}
	parts := make([]string, len(fields))
	for i, f := range fields {
		parts[i] = lowerFirst(GoFieldName(f.Name)) + " " + GoTypeRef(f.Type)
	}
	return strings.Join(parts, ", ")
}

// renderErrorCtorAssigns returns the indented `Name: name,` lines that
// initialise each custom field inside the constructor's struct literal.
// Empty when no custom fields exist.
func renderErrorCtorAssigns(fields []*ast.Field) string {
	if len(fields) == 0 {
		return ""
	}
	var sb strings.Builder
	for _, f := range fields {
		name := GoFieldName(f.Name)
		sb.WriteString(fmt.Sprintf("\t\t%s: %s,\n", name, lowerFirst(name)))
	}
	return sb.String()
}

// errSuffix appends `Err` to name unless name already ends in `Err` or
// `Error` — the smart-suffix rule documented in the README.
func errSuffix(name string) string {
	if strings.HasSuffix(name, "Err") || strings.HasSuffix(name, "Error") {
		return name
	}
	return name + "Err"
}

// screamingSnake converts a PascalCase / camelCase identifier to
// SCREAMING_SNAKE_CASE for use as a default error-code constant. Common
// initialisms ("HTTP", "ID") collapse to their upper form, e.g.
// `UserNotFound` → `USER_NOT_FOUND` and `DBLockedErr` → `DB_LOCKED_ERR`.
func screamingSnake(s string) string {
	parts := splitFieldName(s)
	for i, p := range parts {
		parts[i] = strings.ToUpper(p)
	}
	return strings.Join(parts, "_")
}

// stringDefault returns the string argument of `@default("...")` (the first
// matching decorator), or empty string when no such decorator is present.
func stringDefault(decs []*ast.Decorator) string {
	for _, d := range decs {
		if d.Name != "default" || len(d.Args) == 0 {
			continue
		}
		if s, ok := d.Args[0].Value.(*ast.StringLit); ok {
			return s.Value
		}
	}
	return ""
}

// lowerFirst lowercases the first rune of s. Used to derive constructor
// parameter names from exported Go field names.
func lowerFirst(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}
