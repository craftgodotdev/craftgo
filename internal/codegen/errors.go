// GenerateErrors emits a single `errors.go` file under outDir/<pkg.Name>/
// declaring one struct + constructor + Error()/HTTPStatus() methods +
// SCREAMING_SNAKE error-code constant for every [ast.ErrorDecl] in pkg.
// When pkg has no errors the function is a no-op.
func GenerateErrors(pkg *semantic.Package, outDir string) error {
	if pkg.Name == "" {
		return fmt.Errorf("package has no name")
	}
	if len(pkg.Errors) == 0 {
		return nil
	}
	pkgDir := filepath.Join(outDir, pkg.Name)
	if err := os.MkdirAll(pkgDir, 0o755); err != nil {
		return err
	}
	src := buildErrorsGo(pkg)
	formatted, err := format.Source([]byte(src))
	if err != nil {
		return fmt.Errorf("format errors.go: %w\n--- source ---\n%s", err, src)
	}
	return os.WriteFile(filepath.Join(pkgDir, "errors.go"), formatted, 0o644)
}

// buildErrorsGo assembles errors.go in alphabetical name order. Imports are
// not emitted because the standard library is not required for the basic
// error shape; future @sensitive support will revisit this.
func buildErrorsGo(pkg *semantic.Package) string {
	var sb strings.Builder
	sb.WriteString("// Code generated by craftgo. DO NOT EDIT.\n\n")
	sb.WriteString("package " + pkg.Name + "\n\n")

	names := make([]string, 0, len(pkg.Errors))
	for n := range pkg.Errors {
		names = append(names, n)
	}
	sort.Strings(names)

	for _, name := range names {
		emitError(&sb, pkg.Errors[name])
	}
	return sb.String()
}

// emitError writes the full Go scaffolding for one error declaration:
//
//	const ErrCode<Name> = "<SCREAMING_SNAKE>"
//	type <Name>Err struct { Code, Message string; ...customFields }
//	func New<Name>Err(...customFields) *<Name>Err { ... }
//	func (e *<Name>Err) Error() string  { return e.Message }
//	func (e *<Name>Err) HTTPStatus() int { return <statusCode> }
//
// User-declared `code` / `message` fields with `@default(...)` override the
// derived defaults; any other declared field becomes a constructor
// parameter and a struct field with a JSON tag matching the DSL name.
func emitError(sb *strings.Builder, ed *ast.ErrorDecl) {
	typeName := errSuffix(ed.Name)
	code := screamingSnake(ed.Name)
	msg := categoryMessage[ed.Category]
	status := categoryStatus[ed.Category]

	var customFields []*ast.Field
	for _, m := range ed.Body {
		f, ok := m.(*ast.Field)
		if !ok {
			continue
		}
		switch f.Name {
		case "code":
			if v := stringDefault(f.Decorators); v != "" {
				code = v
			}
		case "message":
			if v := stringDefault(f.Decorators); v != "" {
				msg = v
			}
		default:
			customFields = append(customFields, f)
		}
	}

	constName := "ErrCode" + ed.Name
	sb.WriteString("// " + constName + " is the canonical machine-readable code for " + typeName + ".\n")
	sb.WriteString("const " + constName + " = " + strconv.Quote(code) + "\n\n")

	// Struct definition
	sb.WriteString("// " + typeName + " is the typed " + ed.Category + " error generated for `" + ed.Name + "`.\n")
	sb.WriteString("type " + typeName + " struct {\n")
	sb.WriteString("\tCode    string `json:\"code\"`\n")
	sb.WriteString("\tMessage string `json:\"message\"`\n")
	for _, f := range customFields {
		sb.WriteString("\t")
		sb.WriteString(GoFieldName(f.Name))
		sb.WriteString(" ")
		sb.WriteString(GoTypeRef(f.Type))
		sb.WriteString(" `json:\"")
		sb.WriteString(f.Name)
		sb.WriteString("\"`\n")
	}
	sb.WriteString("}\n\n")

	// Constructor
	sb.WriteString("// New" + typeName + " constructs " + typeName + " with the canonical code and message.\n")
	sb.WriteString("func New" + typeName + "(")
	for i, f := range customFields {
		if i > 0 {
			sb.WriteString(", ")
		}
		sb.WriteString(lowerFirst(GoFieldName(f.Name)))
		sb.WriteString(" ")
		sb.WriteString(GoTypeRef(f.Type))
	}
	sb.WriteString(") *" + typeName + " {\n")
	sb.WriteString("\treturn &" + typeName + "{\n")
	sb.WriteString("\t\tCode: " + constName + ",\n")
	sb.WriteString("\t\tMessage: " + strconv.Quote(msg) + ",\n")
	for _, f := range customFields {
		sb.WriteString("\t\t" + GoFieldName(f.Name) + ": " + lowerFirst(GoFieldName(f.Name)) + ",\n")
	}
	sb.WriteString("\t}\n}\n\n")

	// Error() method
	sb.WriteString("// Error implements the standard error interface.\n")
	sb.WriteString("func (e *" + typeName + ") Error() string { return e.Message }\n\n")

	// HTTPStatus() method
	sb.WriteString("// HTTPStatus returns the HTTP status code associated with the " + ed.Category + " category.\n")
	sb.WriteString("func (e *" + typeName + ") HTTPStatus() int { return " + strconv.Itoa(status) + " }\n\n")

	// WithMessage — runtime override of the canonical Message.
	sb.WriteString("// WithMessage replaces the default Message and returns the receiver\n")
	sb.WriteString("// so callers can chain it onto a return statement.\n")
	sb.WriteString("func (e *" + typeName + ") WithMessage(msg string) *" + typeName + " {\n")
	sb.WriteString("\te.Message = msg\n")
	sb.WriteString("\treturn e\n")
	sb.WriteString("}\n\n")

	// WithCode — runtime override of the canonical Code (rare; use the
	// design-time `code @default(...)` form when the override is constant).
	sb.WriteString("// WithCode replaces the default Code and returns the receiver\n")
	sb.WriteString("// so callers can chain it onto a return statement.\n")
	sb.WriteString("func (e *" + typeName + ") WithCode(code string) *" + typeName + " {\n")
	sb.WriteString("\te.Code = code\n")
	sb.WriteString("\treturn e\n")
	sb.WriteString("}\n\n")
}

// errSuffix appends `Err` to name unless name already ends in `Err` or
// `Error` — the smart-suffix rule documented in the README.
func errSuffix(name string) string {
	if strings.HasSuffix(name, "Err") || strings.HasSuffix(name, "Error") {
		return name
	}
	return name + "Err"
}

// screamingSnake converts a PascalCase / camelCase identifier to
// SCREAMING_SNAKE_CASE for use as a default error-code constant. Common
// initialisms ("HTTP", "ID") collapse to their upper form, e.g.
// `UserNotFound` → `USER_NOT_FOUND` and `DBLockedErr` → `DB_LOCKED_ERR`.
func screamingSnake(s string) string {
	parts := splitFieldName(s)
	for i, p := range parts {
		parts[i] = strings.ToUpper(p)
	}
	return strings.Join(parts, "_")
}

// stringDefault returns the string argument of `@default("...")` (the first
// matching decorator), or empty string when no such decorator is present.
func stringDefault(decs []*ast.Decorator) string {
	for _, d := range decs {
		if d.Name != "default" || len(d.Args) == 0 {
			continue
		}
		if s, ok := d.Args[0].Value.(*ast.StringLit); ok {
			return s.Value
		}
	}
	return ""
}

// lowerFirst lowercases the first rune of s. Used to derive constructor
// parameter names from exported Go field names.
func lowerFirst(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}
