package codegen

import (
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"

	"github.com/dropship-dev/craftgo/internal/ast"
	"github.com/dropship-dev/craftgo/internal/semantic"
)

// GenerateEnums emits a single `enums.go` file under outDir/<pkg.Name>/
// declaring one Go named type plus a const block for every [ast.EnumDecl]
// in pkg. When pkg has no enums the function is a no-op (no file is
// written) so callers can call it unconditionally.
//
// Naming: the underlying Go type is `int` for int-valued enums and `string`
// for everything else (bare and string-valued). Each constant is rendered
// as `<EnumName><PascalCase(ValueName)>`. Bare values use the value name
// itself as the string payload, matching the README convention.
func GenerateEnums(pkg *semantic.Package, outDir string) error {
	if pkg.Name == "" {
		return fmt.Errorf("package has no name")
	}
	if len(pkg.Enums) == 0 {
		return nil
	}
	pkgDir := filepath.Join(outDir, pkg.Name)
	if err := os.MkdirAll(pkgDir, 0o755); err != nil {
		return err
	}
	src := buildEnumsGo(pkg)
	formatted, err := format.Source([]byte(src))
	if err != nil {
		return fmt.Errorf("format enums.go: %w\n--- source ---\n%s", err, src)
	}
	return os.WriteFile(filepath.Join(pkgDir, "enums.go"), formatted, 0o644)
}

// buildEnumsGo assembles the textual contents of enums.go in deterministic
// (alphabetical) order so generated files diff cleanly across runs.
func buildEnumsGo(pkg *semantic.Package) string {
	var sb strings.Builder
	sb.WriteString("// Code generated by craftgo. DO NOT EDIT.\n\n")
	sb.WriteString("package " + pkg.Name + "\n\n")

	names := make([]string, 0, len(pkg.Enums))
	for n := range pkg.Enums {
		names = append(names, n)
	}
	sort.Strings(names)

	for _, name := range names {
		emitEnum(&sb, pkg.Enums[name])
	}
	return sb.String()
}

// emitEnum writes one named type and its associated `const` block. The
// runtime kind (int vs string) is derived from the first value; the
// semantic phase already enforced that all values share a kind.
func emitEnum(sb *strings.Builder, ed *ast.EnumDecl) {
	goBase := "string"
	if firstEnumKind(ed) == ast.EnumInt {
		goBase = "int"
	}
	sb.WriteString("type " + ed.Name + " " + goBase + "\n\n")

	sb.WriteString("const (\n")
	for _, v := range ed.Values {
		sb.WriteString("\t")
		sb.WriteString(ed.Name + GoFieldName(v.Name))
		sb.WriteString(" ")
		sb.WriteString(ed.Name)
		sb.WriteString(" = ")
		switch v.Kind {
		case ast.EnumInt:
			sb.WriteString(strconv.FormatInt(v.IntValue, 10))
		case ast.EnumString:
			sb.WriteString(strconv.Quote(v.StrValue))
		default: // EnumBare — use the source name as the string payload
			sb.WriteString(strconv.Quote(v.Name))
		}
		sb.WriteString("\n")
	}
	sb.WriteString(")\n\n")
}

// firstEnumKind returns the kind of the first value, or [ast.EnumString]
// for an empty enum (defensive — semantic should have rejected empty enums).
func firstEnumKind(ed *ast.EnumDecl) ast.EnumValueKind {
	if len(ed.Values) == 0 {
		return ast.EnumString
	}
	return ed.Values[0].Kind
}
